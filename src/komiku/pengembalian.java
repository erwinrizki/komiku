    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        
        try {
            k.st = k.con.createStatement();
            String tampil = "SELECT * FROM peminjaman WHERE id_smartcard = '" +id+ "' AND status = 'Dipinjam';";
            k.rs = k.st.executeQuery(tampil);
            while(k.rs.next()) {
                data[0][0] = k.rs.getString("id_komik");
                data[0][1] = k.rs.getString("judul_komik");
                data[0][2] = k.rs.getString("volume");
                data[0][3] = k.rs.getString("harga_sewa");
                data[0][4] = k.rs.getString("tanggalpinjam");
                
                Object dt[] = {data[0][0],data[0][1],data[0][2],data[0][3],data[0][4]};
                dtm.insertRow(idx, dt);
                idx++;
                jButton1.setEnabled(false);
                jButton2.setEnabled(true);
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error: " +e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String tglpinjam = String.valueOf(jTable1.getValueAt(0, 4));
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String t1;
        
        try {
            t1 = tglpinjam;
            Date d1 = df.parse(String.valueOf(tglpinjam));
            Date d2 = df.parse(tanggal1);
            
            long Hari1 = d1.getTime();
            long Hari2 = d2.getTime();
            long x = Hari2 - Hari1;
            long lama = x/(24*60*60*1000);
            
            if(lama==1) {
                jTextField2.setText("0");
            } else {
                denda = (lama * 1000) - 1000;
                totaldenda = dtm.getRowCount() * denda;
                jTextField2.setText("" +totaldenda);
            }
            jButton3.setEnabled(true);
        } catch(Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error: " +e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

