    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        
        HapusTabel();
        idx = 0;
        //jTextField2.setText("");
        try {
            k.st = k.con.createStatement();
            String tampil = "SELECT * FROM pinjamtemp WHERE id_smartcard = '" +id+ "';";
            k.rs = k.st.executeQuery(tampil);
            
            while(k.rs.next()) {
                String id1 = k.rs.getString("id_smartcard");
                String idb1 = k.rs.getString("id_buku");
                String jd1 = k.rs.getString("judul_buku");
                String vol1 = k.rs.getString("volume");
                String harga1 = k.rs.getString("harga");
                    
                Object dt[] = {idb1,jd1,vol1,harga1};
                dtm.insertRow(idx, dt);
                idx++;
                jButton2.setEnabled(true);
                jTextField1.setEditable(false);
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error: " +e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        
        try {
            try {
                k.st = k.con.createStatement();
                String tampil = "SELECT * FROM datauser WHERE id_smartcard = '" +id+ "';";
                k.rs = k.st.executeQuery(tampil);
                if(k.rs.next()) {
                    String sd = k.rs.getString("saldo");
                    saldo = Integer.parseInt(sd);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Error");
                }
            } catch(Exception e) {
                
            }
            
            k.st = k.con.createStatement();
            String tampil1 = "SELECT * FROM pinjamtemp WHERE id_smartcard = '" +id+ "';";
            k.rs = k.st.executeQuery(tampil1);
            while(k.rs.next()) {
                String t = k.rs.getString("harga");
                total = total + Integer.parseInt(t);
                
            }
            
            if(total>saldo) {
                JOptionPane.showMessageDialog(rootPane, "Saldo Tidak Mencukupi");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Saldo Mencukupi");
                jButton3.setEnabled(true);
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error: " +e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

