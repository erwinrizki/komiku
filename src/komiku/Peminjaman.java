    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        int baris;
        int no = 1;
        int total = 0;
        int saldo = 0;
        int pers = 0;
        
        for(baris = 0;baris<jTable1.getRowCount();baris++) {
            String idk = String.valueOf(jTable1.getValueAt(baris, 0));
            String jd = String.valueOf(jTable1.getValueAt(baris, 1));
            String vol = String.valueOf(jTable1.getValueAt(baris, 2));
            String harga = String.valueOf(jTable1.getValueAt(baris, 3));
            
            try {
                try {
                    k.st = k.con.createStatement();
                    String tampil = "SELECT * FROM peminjaman";
                    k.rs = k.st.executeQuery(tampil);
                    while(k.rs.next()) {
                        no = Integer.parseInt(k.rs.getString("no_trans"));
                        no++;
                    }
                } catch(Exception e) {
                    
                }
                
                try {
                    k.st = k.con.createStatement();
                    String t = "SELECT * FROM datauser WHERE id_smartcard = '" +id+ "';";
                    k.rs = k.st.executeQuery(t);
                    if(k.rs.next()) {
                        saldo = Integer.parseInt(k.rs.getString("saldo"));
                    }
                } catch(Exception e) {
                    
                }
                
                total = total + Integer.parseInt(harga);
                saldo = saldo - total;
                
                k.st = k.con.createStatement();
                String tambah = "INSERT INTO peminjaman VALUES('" +no+ "','" +id+ "','" +idk+ "','" +jd+ "','" +vol+ "','" +harga+ "','" +tanggal1+ "','Dipinjam');";
                k.st.executeUpdate(tambah);
                JOptionPane.showMessageDialog(rootPane, "Data Berhasil Ditambahkan");
                
                try {
                    k.st = k.con.createStatement();
                    String u = "UPDATE datauser SET saldo = '" +saldo+ "' WHERE id_smartcard = '" +id+ "';";
                    k.st.executeUpdate(u);
                    JOptionPane.showMessageDialog(rootPane, "Saldo Telah Diupdate");
                } catch(Exception e) {
                    JOptionPane.showMessageDialog(rootPane, "Saldo Gagal Diupdate: " +e);
                }
                
                try {
                    k.st = k.con.createStatement();
                    String d = "SELECT * FROM databuku WHERE id_buku = '" +idk+ "';";
                    k.rs = k.st.executeQuery(d);
                    if(k.rs.next()) {
                        pers = Integer.parseInt(k.rs.getString("persediaan"));
                    }
                } catch(Exception e) {
                    
                }
                
                pers--;
                
                try {
                    k.st = k.con.createStatement();
                    String u = "UPDATE databuku SET persediaan = '" +pers+ "' WHERE id_buku = '" +idk+ "';";
                    k.st.executeUpdate(u);
                    JOptionPane.showMessageDialog(rootPane, "Data Buku Berhasil Diupdate");
                } catch(Exception e) {
                    JOptionPane.showMessageDialog(rootPane, "Data Buku Gagal Diupdate: " +e);
                }
                
                try {
                    k.st = k.con.createStatement();
                    String del = "DELETE FROM pinjamtemp WHERE id_smartcard = '" +id+ "';";
                    k.st.executeUpdate(del);
                } catch(Exception e) {
                    JOptionPane.showMessageDialog(rootPane, "Data Gagal Diupdate: " +e);
                }
            } catch(Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Error: " +e);
            }
        }
        
        jTextField2.setText("" +total);
        jTextField1.setText("");
        jDateChooser1.setDate(null);
        jButton3.setEnabled(false);
    }//GEN-LAST:event_jButton3ActionPerformed

